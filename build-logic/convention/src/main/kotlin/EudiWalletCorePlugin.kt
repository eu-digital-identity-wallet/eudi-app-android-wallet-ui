class EudiWalletCorePlugin : Plugin<Project> {
    override fun apply(target: Project) {
        with(target) {
            with(pluginManager) {
                apply("kotlinx-serialization")
            }

            dependencies {
                add("api", libs.findLibrary("eudi.wallet.core").get())
                add("implementation", libs.findLibrary("cbor-tree").get())
            }
        }
    }
}